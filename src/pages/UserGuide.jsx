function Section({ section }) {
  const [open, setOpen] = useState(true);
  return (
    <div className="mb-5 border border-border rounded-xl overflow-hidden shadow-sm" id={section.id}>
      <button
        onClick={() => setOpen(o => !o)}
        className="w-full flex items-center gap-3 px-5 py-4 bg-muted/40 hover:bg-muted/70 transition-colors text-right"
      >
        <span className="text-2xl">{section.icon}</span>
        <span className="flex-1 font-bold">{section.title}</span>
        {open
          ? <ChevronDown className="h-4 w-4 text-muted-foreground" />
          : <ChevronRight className="h-4 w-4 text-muted-foreground" />}
      </button>

      {open && (
        <div className="bg-card">
          {/* صورة الشاشة */}
          {section.screenshot && (
            <div className="mx-5 mt-4 rounded-lg overflow-hidden border border-border shadow-md">
              <img
                src={section.screenshot}
                alt={`صورة توضيحية لـ ${section.title}`}
                className="w-full object-cover max-h-56"
                loading="lazy"
              />
              <p className="text-xs text-center text-muted-foreground py-1.5 bg-muted/30">
                📷 واجهة {section.title}
              </p>
            </div>
          )}

          {/* المحتوى */}
          <div className="px-5 py-4 space-y-5">
            {section.content.map((item, i) => (
              <div key={i} className="border-r-2 border-primary/30 pr-3">
                <h4 className="font-semibold text-sm text-primary mb-1.5">{item.subtitle}</h4>
                <p className="text-sm text-muted-foreground leading-relaxed whitespace-pre-line">{item.text}</p>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}

