// الصفحة الرئيسية
// ──────────────────────────────────────────────────────────────────────────────
export default function UserGuide() {
  const contentRef = useRef(null);
  const [exporting, setExporting] = useState(false);
  const [search, setSearch] = useState("");
  const [showTop, setShowTop] = useState(false);

  // Show scroll-to-top button
  if (typeof window !== "undefined") {
    window.onscroll = () => setShowTop(window.scrollY > 300);
  }

  const filteredSections = SECTIONS.filter(s =>
    !search ||
    s.title.includes(search) ||
    s.content.some(c => c.subtitle.includes(search) || c.text.includes(search))
  );

  async function exportToPDF() {
    setExporting(true);
    const el = contentRef.current;
    const clone = el.cloneNode(true);
    clone.style.cssText = "width:900px;position:absolute;top:-9999px;background:#fff;padding:32px;direction:rtl;font-family:Arial,sans-serif;";
    document.body.appendChild(clone);
    const canvas = await html2canvas(clone, { scale: 1.5, useCORS: true, backgroundColor: "#ffffff" });
    document.body.removeChild(clone);
    const imgData = canvas.toDataURL("image/jpeg", 0.88);
    const pdf = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
    const pageW = pdf.internal.pageSize.getWidth();
    const pageH = pdf.internal.pageSize.getHeight();
    const imgW = pageW - 20;
    const imgH = (canvas.height * imgW) / canvas.width;
    let posY = 10;
    let remaining = imgH;
    while (remaining > 0) {
      pdf.addImage(imgData, "JPEG", 10, posY, imgW, imgH);
      remaining -= (pageH - 20);
      if (remaining > 0) { pdf.addPage(); posY = 10 - (imgH - remaining); }
    }
    pdf.save("دليل_استخدام_ETQAN_ERP.pdf");
    setExporting(false);
  }

  return (
    <div className="max-w-4xl mx-auto p-4 md:p-6">
      {/* Header */}
      <div className="flex items-start justify-between mb-6 gap-4 flex-wrap">
        <div>
          <h1 className="text-2xl font-bold flex items-center gap-2">
            <BookOpen className="h-6 w-6 text-primary" />
            دليل استخدام النظام
          </h1>
          <p className="text-muted-foreground text-sm mt-1">ETQAN ERP — الدليل الشامل لجميع وحدات النظام</p>
          <div className="flex gap-2 mt-2 flex-wrap">
            <Badge variant="secondary">الإصدار v3.0</Badge>
            <Badge variant="outline">آخر تحديث: يونيو 2026</Badge>
            <Badge variant="outline">{SECTIONS.length} وحدة</Badge>
          </div>
        </div>
        <div className="flex gap-2 shrink-0">
          <Button variant="outline" onClick={() => window.print()} className="gap-2">
            <Printer className="h-4 w-4" />طباعة
          </Button>
          <Button onClick={exportToPDF} disabled={exporting} className="gap-2">
            <FileDown className="h-4 w-4" />
            {exporting ? "جاري التصدير..." : "تصدير PDF"}
          </Button>
        </div>
      </div>

      {/* بحث */}
      <div className="relative mb-5">
        <Search className="absolute right-3 top-2.5 h-4 w-4 text-muted-foreground" />
        <Input
          className="pr-9"
          placeholder="ابحث في الدليل..."
          value={search}
          onChange={e => setSearch(e.target.value)}
        />
      </div>

      {/* فهرس المحتويات */}
      {!search && (
        <div className="mb-6 p-4 rounded-xl border border-border bg-muted/20">
          <h3 className="font-semibold text-sm mb-3 flex items-center gap-2">
            <span>📑</span> فهرس المحتويات
          </h3>
          <div className="grid grid-cols-2 sm:grid-cols-3 gap-1.5">
            {SECTIONS.map(s => (
              <a
                key={s.id}
                href={`#${s.id}`}
                className="flex items-center gap-1.5 text-xs text-muted-foreground hover:text-primary transition-colors py-1 px-2 rounded hover:bg-muted/50"
              >
                <span>{s.icon}</span>
                <span>{s.title}</span>
              </a>
            ))}
          </div>
        </div>
      )}

      {/* المحتوى */}
      <div ref={contentRef}>
        {filteredSections.length === 0
          ? <p className="text-center py-16 text-muted-foreground">لا توجد نتائج لـ "{search}"</p>
          : filteredSections.map(section => <Section key={section.id} section={section} />)
        }
      </div>

      {/* تذييل */}
      <div className="mt-6 p-4 rounded-xl bg-primary/5 border border-primary/20 text-center">
        <p className="text-sm text-muted-foreground">
          لأي استفسار أو دعم فني، تواصل معنا عبر صفحة{" "}
          <a href="/contact" className="text-primary font-medium hover:underline">تواصل معنا</a>
        </p>
        <p className="text-xs text-muted-foreground mt-1">ETQAN ERP © 2026 — جميع الحقوق محفوظة</p>
      </div>

      {/* زر الرجوع للأعلى */}
      {showTop && (
        <button
          onClick={() => window.scrollTo({ top: 0, behavior: "smooth" })}
          className="fixed bottom-6 left-6 z-50 bg-primary text-primary-foreground rounded-full w-10 h-10 flex items-center justify-center shadow-lg hover:bg-primary/90 transition"
        >
          <ArrowUp className="h-5 w-5" />
        </button>
      )}
    </div>
  );
}